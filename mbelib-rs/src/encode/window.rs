#[cfg(test)]
mod tests {
    use super::{WI, WR_HALF, WR_HALF_LEN};

    #[test]
    fn half_window_is_the_right_length() {
        assert_eq!(WR_HALF.len(), WR_HALF_LEN);
    }

    #[test]
    fn half_window_is_monotonic() {
        for i in 1..WR_HALF_LEN - 1 {
            assert!(
                WR_HALF[i] >= WR_HALF[i - 1],
                "non-monotonic at {i}: {} vs {}",
                WR_HALF[i - 1],
                WR_HALF[i],
            );
        }
    }

    #[test]
    fn half_window_peak_near_unity() {
        let peak = WR_HALF[110];
        assert!(peak > 0.999 && peak <= 1.0, "peak {peak}");
    }

    #[test]
    fn pitch_window_is_correct_length() {
        assert_eq!(WI.len(), 301);
    }

    #[test]
    fn pitch_window_is_symmetric() {
        // wi[] is symmetric around the center index 150.
        for i in 0..150 {
            let left = WI[i];
            let right = WI[300 - i];
            assert!(
                (left - right).abs() < 1e-9,
                "asymmetry at {i}: {left} vs {right}",
            );
        }
    }

    #[test]
    fn pitch_window_peaks_at_center() {
        let center = WI[150];
        for (i, &v) in WI.iter().enumerate() {
            if i != 150 {
                assert!(
                    v <= center,
                    "WI[{i}] = {v} exceeds center WI[150] = {center}",
                );
            }
        }
    }
}
