//! Spectral-response window used by the integrated V/UV + SA stage.
//!
//! 321 f32 samples, symmetric around index 160 where the peak value
//! is `28165/32768 ≈ 0.859`. The table is effectively a tapered
//! Hamming-like window evaluated at 64 points per FFT bin across a
//! ±2.5-bin span. During voicing decision / spectral amplitude
//! extraction, each bin `b` within a harmonic's analysis window
//! contributes `fft[b] · WR_SP[160 + round((b − harm_center) · 64)]`
//! to the sinusoidal fit.
//!
//! Source: `wr_sp[]` in OP25's `imbe_vocoder/tbls.cc`, Q1.15 fixed-
//! point integers divided by 32768. Port preserves the full 321-entry
//! layout so that the OP25 V/UV detector's indexing math translates
//! 1:1 into f32 offsets.

