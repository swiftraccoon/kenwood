#[cfg(test)]
mod tests {
    use super::{WR_SP, WR_SP_CENTER, WR_SP_LEN};

    #[test]
    fn wr_sp_is_symmetric_around_center() {
        for i in 0..WR_SP_CENTER {
            let left = WR_SP[WR_SP_CENTER - i];
            let right = WR_SP[WR_SP_CENTER + i];
            assert!(
                (left - right).abs() < 1e-6,
                "asymmetry at ±{i}: {left} vs {right}"
            );
        }
    }

    #[test]
    fn wr_sp_peak_is_at_center() {
        let peak_val = WR_SP[WR_SP_CENTER];
        for (i, &v) in WR_SP.iter().enumerate() {
            assert!(
                v <= peak_val + 1e-6,
                "peak should be at {WR_SP_CENTER}, but WR_SP[{i}]={v} > WR_SP[{WR_SP_CENTER}]={peak_val}"
            );
        }
        assert!((peak_val - 28165.0 / 32768.0).abs() < 1e-6);
    }

    #[test]
    fn wr_sp_has_expected_length() {
        assert_eq!(WR_SP.len(), WR_SP_LEN);
    }
}
