//   AmbePlusDg     -> DG_TABLE  (64 entries, 6-bit b2 gain delta)
//   AmbePlusPRBA24 -> PRBA24_TABLE (512x3, 9-bit b3 PRBA low-band)
//   AmbePlusPRBA58 -> PRBA58_TABLE (128x4, 7-bit b4 PRBA high-band)
//   AmbePlusHOCb5  -> HOC_B5_TABLE (16x4, 4-bit b5 higher-order coeffs)
//   AmbePlusHOCb6  -> HOC_B6_TABLE (16x4, 4-bit b6 higher-order coeffs)
//   AmbePlusHOCb7  -> HOC_B7_TABLE (16x4, 4-bit b7 higher-order coeffs)
//   AmbePlusHOCb8  -> HOC_B8_TABLE (16x4, 4-bit b8 higher-order coeffs)
//
// Unlike our prior (incorrect) 2450/AMBE+2 tables, these are the real
// D-STAR codec parameter quantization codebooks.

// Codec tables are verbatim copies of the mbelib reference values.
// Clippy's style lints ("long literal lacking separators", "float has
// excessive precision") flag thousands of entries here — silencing
// them at module scope both keeps the upstream values byte-for-byte
// identical to the C source and avoids a review burden every time
// upstream changes. NOT the place to start applying style fixups.
#![allow(
    clippy::unreadable_literal,
    clippy::excessive_precision,
    reason = "verbatim port of mbelib 2400 codebook values"
)]

